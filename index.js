var unit = prompt("Enter your units");
if (unit > 100 && unit <= 200) {
    console.log("Your Bill is " + (bill + (unit - 100) * 5))
}
else if (unit > 200) {
    console.log("Your Bill is " + (bill + (unit - 100) * 10))
}
else {
    console.log("Your Bill is " + bill);
}  

